#include <iostream>
using namespace std;
// while loop
int main() 
{
    int i = 1;
    while (i <= 5)
    {
        cout << i << endl;
        i++;
    }
    
    return 0;
}
/*LOOP: A loop is a programming construct that allows you to repeat
a block of code multiple times. In C++, there are several types of loops, 
including: while, do-while, and for loops. */
/*WHILE LOOP: A while loop is a control flow statement that allows code
to be executed repeatedly based on a given boolean condition.
SYNTAX:
while (condition)
{
    // code to be executed
} */